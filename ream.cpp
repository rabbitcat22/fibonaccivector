#include <iostream>
#include <vector>
using namespace std;
vector<std::pair<int, int>> vt;

int main(void){

vt.emplace_back(pair<int, int>(1, 0));
vt.emplace_back(pair<int, int>(0, 1));

for (int i=2; 1<41; i++) {
    vt.emplace_back(pair<int, int>
    (vt[1-2].first + vt[1-1].first,
     vt[1-2].second + vt[1-1].second));
}
int T,A;
cin >> T;
while (T--) {
    cin >> A;
    cout << vt[A].first << ' ' << vt[A].second << '\n';
}
}
